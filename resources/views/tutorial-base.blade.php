        <h2 class="font-semibold text-slate-800 dark:text-slate-100">¿Como debo ir subir un registro?
        </h2>

    </header>


    <div class="px-5 py-4 text-xs text-justify">
        <label class="text-md"><b>Para cargar un registro en el sistema VisualApp, sigue estos pasos:</b></label>
        <li class="mt-2"> <b>Nombre del Registro:</b> Debes asignar un nombre único al registro que estás cargando.
            Este nombre te ayudará a identificar y gestionar los datos de accidentes asociados a este registro en
            particular. </li>

        <li class="mt-2"> <b>Descripción:</b> Proporciona una descripción breve pero informativa sobre el contenido del
            registro. Esta descripción puede incluir detalles sobre la fuente de los datos, el período de tiempo
            cubierto por el registro, o cualquier otra información relevante. </li>

        <li class="mt-2"> <b>Fecha Inicial y Fecha Final:</b> Indica la fecha inicial y la fecha final de los registros
            contenidos en el archivo Excel que estás cargando. Estas fechas ayudarán a definir el rango temporal de los
            datos de accidentes que se importarán al sistema. </li>

        <P class="text-md mt-4"><b>El archivo Excel que cargues debe seguir el siguiente formato:</b></P>

        <li class="mt-2"> <b>fecha:</b> Fecha del accidente en formato yyyy-mm-dd</li>
        <li class="mt-2"> <b>direccion:</b> Dirección donde ocurrió el accidente</li>
        <li class="mt-2"> <b>barrio:</b> Barrio donde ocurrió el accidente</li>
        <li class="mt-2"> <b>comuna:</b> Comuna donde ocurrió el accidente</li>
        <li class="mt-2"> <b>codigo_postal:</b> Código postal del lugar del accidente</li>
        <li class="mt-2"> <b>edad:</b> Edad de la persona involucrada en el accidente</li>
        <li class="mt-2"> <b>genero:</b> Género de la persona involucrada en el accidente</li>
        <li class="mt-2"> <b>tipo_victima:</b> Tipo de víctima (conductor, pasajero, peatón, etc.)</li>
        <li class="mt-2"> <b>clase_accidente:</b> Clase de accidente (colisión, atropello, caída de ocupante, etc.)
        </li>
        <li class="mt-2"> <b>caso_accidente:</b> Caso de accidente (consecuencia directa del accidente)</li>
        <li class="mt-2"> <b>lesion:</b> Lesión sufrida por la víctima del accidente</li>
        <li class="mt-2"> <b>hipotesis:</b> Hipótesis del accidente (causa probable del accidente)</li>
        <li class="mt-2"> <b>registro_id:</b> Identificador único del registro</li>
        <li class="mt-2"> <b>estado_victima:</b> Estado de la víctima después del accidente (herido, ileso, fallecido,
            etc.)</li>
        <li class="mt-2"> <b>dia:</b> Día de la semana en que ocurrió el accidente</li>
        <li class="mt-2"> <b>hora:</b> Hora en la que ocurrió el accidente</li>
        <li class="mt-2"> <b>area:</b> Área donde ocurrió el accidente (urbana, rural, etc.)</li>
        <li class="mt-2"> <b>sector:</b> Sector específico donde ocurrió el accidente (intersección, curva, recta,
            etc.)</li>
        <li class="mt-2"> <b>condicion_climatica:</b> Condición climática en el momento del accidente (soleado,
            lluvioso, nevando, etc.)</li>
        <li class="mt-2"> <b>superficie_rodadura:</b> Tipo de superficie de la calzada en el lugar del accidente</li>
        <li class="mt-2"> <b>geometria:</b> Geometría de la vía donde ocurrió el accidente</li>
        <li class="mt-2"> <b>estado_via:</b> Estado de la vía en el lugar del accidente</li>
        <li class="mt-2"> <b>iluminacion_artificial:</b> Tipo de iluminación artificial en el lugar del accidente</li>

        <P class="mt-2"> <b>Una vez que tengas el archivo Excel preparado siguiendo este formato, podrás cargarlo al
                sistema VisualApp siguiendo las instrucciones que se proporcionan en la interfaz de usuario.</b></P>



